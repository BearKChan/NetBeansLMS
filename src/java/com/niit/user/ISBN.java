package com.niit.user;

/**
 *
 * @author Bear
 */
public class ISBN {

    private int isbn_id;
    private String book_ISBN;
    private String book_name;
    private String book_category;
    private String book_amounts;
    private String book_remains;
    private String book_times;

    /**
     * @return the isbn_id
     */
    public int getIsbn_id() {
        return isbn_id;
    }

    /**
     * @param isbn_id the isbn_id to set
     */
    public void setIsbn_id(int isbn_id) {
        this.isbn_id = isbn_id;
    }

    /**
     * @return the book_ISBN
     */
    public String getBook_ISBN() {
        return book_ISBN;
    }

    /**
     * @param book_ISBN the book_ISBN to set
     */
    public void setBook_ISBN(String book_ISBN) {
        this.book_ISBN = book_ISBN;
    }

    /**
     * @return the book_name
     */
    public String getBook_name() {
        return book_name;
    }

    /**
     * @param book_name the book_name to set
     */
    public void setBook_name(String book_name) {
        this.book_name = book_name;
    }

    /**
     * @return the book_category
     */
    public String getBook_category() {
        return book_category;
    }

    /**
     * @param book_category the book_category to set
     */
    public void setBook_category(String book_category) {
        this.book_category = book_category;
    }

    /**
     * @return the book_amounts
     */
    public String getBook_amounts() {
        return book_amounts;
    }

    /**
     * @param book_amounts the book_amounts to set
     */
    public void setBook_amounts(String book_amounts) {
        this.book_amounts = book_amounts;
    }

    /**
     * @return the book_remains
     */
    public String getBook_remains() {
        return book_remains;
    }

    /**
     * @param book_remains the book_remains to set
     */
    public void setBook_remains(String book_remains) {
        this.book_remains = book_remains;
    }

    /**
     * @return the book_times
     */
    public String getBook_times() {
        return book_times;
    }

    /**
     * @param book_times the book_times to set
     */
    public void setBook_times(String book_times) {
        this.book_times = book_times;
    }

}
